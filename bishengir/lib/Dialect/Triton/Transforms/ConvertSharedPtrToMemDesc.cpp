// This pass runs immediately after ConvertTritonToTritonGPU.  It finds
// tt.load / tt.store operations whose pointer tensors have address-space 6
// (shared memory / UB) and rewrites them to ttg.local_load / ttg.local_store
// with ttg.memdesc types.
//
// The typical input pattern (generated by HIVMToTriton for shared-memory
// accesses) is:
//
//   %splat  = tt.splat  %arg : !tt.ptr<f32, 6> -> tensor<Nx!tt.ptr<f32, 6>>
//   %addptr = tt.addptr %splat, %offsets : ...
//   tt.store %addptr, %data : tensor<Nx!tt.ptr<f32, 6>, #blocked>
//
// After this pass the IR becomes:
//
//   // %arg type changed:  !tt.ptr<f32, 6>  -->  !ttg.memdesc<Nxf32, #shared>
//   ttg.local_store %data, %arg : tensor<Nxf32, #blocked>
//                                  -> !ttg.memdesc<Nxf32, #shared, #smem>
//
//===----------------------------------------------------------------------===//

